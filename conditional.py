# In python if - else or if-elif-elif-else statements are used for conditional branching.

grade = input("Enter your Grade")



if grade == "D":
	print("You have failed in Exams")

elif grade == "C":
	print("You need to work hard")

elif grade=="B":
	print("Your performance is above average")

elif grade == "A":
	print("Bravo, You have passed with bright colors")

else:
	print("This is not in criteria")